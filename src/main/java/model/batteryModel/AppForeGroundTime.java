package model.batteryModel;

public class AppForeGroundTime {

    private String apkName;
    private float foregroundTime;
    private int startCount;

    public String getApkName() {
        return apkName;
    }

    public void setApkName(String apkName) {
        this.apkName = apkName;
    }

    public float getForegroundTime() {
        return foregroundTime;
    }

    public void setForegroundTime(float foregroundTime) {
        this.foregroundTime = foregroundTime;
    }

    public int getStartCount() {
        return startCount;
    }

    public void setStartCount(int startCount) {
        this.startCount = startCount;
    }

}
